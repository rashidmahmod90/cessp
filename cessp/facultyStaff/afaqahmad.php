       <p>1.	Atomic and Molecular Physics (Ion-Photon Interaction) :</p>
       <p style="margin-left: 1%;text-align: justify-all;">The main area of research work is the photo-detachment of atomic and molecular negative ions near the interfaces. These interfaces may be insulator, dielectric or metals that induce oscillations in the total photo-detachment cross section after the removal of extra electron from the negative ion by laser polarized light. To investigate the cause of oscillations in cross section of Hydrogen negative ion near interfaces, I developed a theoretical imaging method under the supervision of Prof. Dr. Du Ming Li during my PhD research work. This method is straight forward that provides a help to find an analytical expression for total photo-detachment cross section whose asymptotic value for high energy falls back to the semi-classical closed orbit theory. I also developed a two-center model with Prof. Du Ming Li to investigate the structural analysis of a diatomic negative ion and successfully applied it to study H2-. Still I am working on above research area with different negative ions and produced a number of publications in international peer reviewed journals. Dr. Amin-ur Rehman and Dr. Muhammad Hanif from Hazara University have done their PhD research work under my co-supervision. Their PhD research titles are Photo-detachment of Linear Negative Ions Systems and Photo-detachment of negative ions near spherical surfaces respectively.</p>
