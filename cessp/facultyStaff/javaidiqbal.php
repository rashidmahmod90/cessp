        <div class="container personal_information_heading">Research Interests and Current Work</div>
        <div class="container" align="justify">
        <p>During my studies I have always been intrigued by the physical phenomena at the nanoscale level. For my Ph.D. research I performed experiments that aimed at improving the understanding of physics in nanoscale electronic devices. The work was on the electrical transport properties of Quantum Point Contacts (QPCs), with a focus on electron many-body states that can form in these devices. These have a significant influence on the transport properties, and a well-known expression of these effects is the so-called 0.7 anomaly. Explaining these effects is a long-standing puzzle (since 1996) in this field. To solve this puzzle, I adopted in my Ph.D. research a new approach by developing QPCs with an in-situ tuning of the length of the transport channel (publication 3). My measurements with these devices led to the discovery of a periodic dependence of the 0.7 anomaly on the channel length, and provide evidence that the origin of the 0.7 anomaly lies in emergent electron localization due to electron many-body effects (publication 1).
Nanoscience has the potential to revolutionize the world and I want work at the forefront of research that explores the new physical phenomena that provide the foundations of this development.
At present I’m working as an assistant prof. in the Centre of Excellence in Solid State Physics, University of the Punjab, Lahore, Pakistan. My job responsibilities here are to teach MS students and conduct research work. Currently I’m working on a research project to make suitable tensile sensors of electricity. We are using a rubber material that can absorb different conducting nanoparticles, hence changing the insulator behavior of the rubber to conductive one. The conductance of the rubber is expected to change as it is stretched.
My main interest is in the electrical and spin transport properties at the nanoscale level.
</p></div>
