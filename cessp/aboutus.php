<p>The Centre of Excellence (COE) in Solid State Physics (SSP) is a Federally funded research institute established under an act of Pakistan Parliament known as Centres of Excellence Act 1974. The major aim of establishing the Centre in this field was to provide the country with technically trained manpower and to establish research facilities in the field of solid state physics. It is one of the 12 Centres of Excellence established in various parts of the country. COE in SSP is situated in Lahore (north-east of Pakistan) at the Quaid-I-Azam campus of University of the Punjab.
COE in Solid State Physics introduced M.Phil. degree in Solid State Physics soon after its inception; it was a pioneering step and a giant leap in education at that time. Since then, over 400 students have been awarded M.Phil. degrees by University of the Punjab on the research work done at COE in Solid State Physics. The Centre, thus, has been very successful in providing technically trained manpower to the country in the field of Solid State Physics (Theoretical &amp; Experimental); these graduates are serving not only the educational institutes of the Country they are also contributing towards R&amp;D through various Government organizations.
The COE in SSP is now equipped with adequate research facilities to offer the following 3 degree programmes.</p>
